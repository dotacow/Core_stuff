#include	<unistd.h>

char	*ft_strupcase(char *str)
{
	char	*rtr;

	rtr = str;
	while (*str != '\0')
	{
		if (*str >= 'a' && *str <= 'z')
		{
			*str = *str - 32;
		}
		str++;
	}
	return (rtr);
}
