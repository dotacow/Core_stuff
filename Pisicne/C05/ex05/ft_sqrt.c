#include	<stdio.h>

int	ft_sqrt(int nb)
{
	int	i;
	int	j;

	j = 0;
	i = 1;
	if (nb <= 0)
		return (0);
	while (i * i != nb)
	{
		i++;
		j++;
		if (j > nb)
			return (0);
	}
	return (i);
}
